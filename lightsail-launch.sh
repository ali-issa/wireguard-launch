if [ "$ENABLE_PORTAL" = true ]; then
  echo "[wg-launch] configuring web portal..."
  id -u wgportal >/dev/null 2>&1 || \
    useradd --system --home-dir /opt/wg-portal --shell /usr/sbin/nologin wgportal
  install -d -m 755 /opt/wg-portal
  # 0755 so nginx (www-data) can traverse in to read htpasswd; the secret FILES
  # inside stay 0640 (proxy-token root:wgportal, htpasswd root:www-data), so a
  # world-searchable directory leaks nothing.
  install -d -m 755 -o root -g wgportal /etc/wg-portal
  install -d -m 750 /etc/ssl/wg-portal

  # The portal user may run ONLY the helper, only as root, without a password.
  echo 'wgportal ALL=(root) NOPASSWD: /usr/local/sbin/wg-portal-helper' > /etc/sudoers.d/wg-portal
  chmod 440 /etc/sudoers.d/wg-portal
  visudo -cf /etc/sudoers.d/wg-portal >/dev/null

  # Shared secret so the app only answers requests proxied by nginx.
  [ -s /etc/wg-portal/proxy-token ] || openssl rand -hex 32 > /etc/wg-portal/proxy-token
  chown root:wgportal /etc/wg-portal/proxy-token
  chmod 640 /etc/wg-portal/proxy-token
  PROXY_TOKEN="$(cat /etc/wg-portal/proxy-token)"

  # Basic-auth password — generated once, recorded for the admin.
  if [ ! -s /etc/wg-portal/htpasswd ]; then
    PORTAL_PW="$(openssl rand -hex 16)"
    htpasswd -bc /etc/wg-portal/htpasswd "$PORTAL_USER" "$PORTAL_PW" >/dev/null
    cat > /root/wg-portal-credentials.txt <<CRED
WireGuard portal
URL:      https://${PUBLIC_IP:-<server-ip>}/
Username: ${PORTAL_USER}
Password: ${PORTAL_PW}
CRED
    chmod 600 /root/wg-portal-credentials.txt
  fi
  chown root:www-data /etc/wg-portal/htpasswd
  chmod 640 /etc/wg-portal/htpasswd

  # Self-signed TLS certificate (browsers warn for IP-only certs — expected).
  if [ ! -s /etc/ssl/wg-portal/portal.crt ]; then
    if [ -n "${PUBLIC_IP:-}" ]; then
      openssl req -x509 -nodes -newkey rsa:2048 -days 3650 \
        -keyout /etc/ssl/wg-portal/portal.key -out /etc/ssl/wg-portal/portal.crt \
        -subj "/CN=${PUBLIC_IP}" -addext "subjectAltName=IP:${PUBLIC_IP}" >/dev/null 2>&1
    else
      openssl req -x509 -nodes -newkey rsa:2048 -days 3650 \
        -keyout /etc/ssl/wg-portal/portal.key -out /etc/ssl/wg-portal/portal.crt \
        -subj "/CN=wg-portal" >/dev/null 2>&1
    fi
  fi
  chmod 600 /etc/ssl/wg-portal/portal.key

  # Wire the proxy token into the nginx vhost and enable it.
  sed -i "s|PROXY_TOKEN_PLACEHOLDER|${PROXY_TOKEN}|g" /etc/nginx/sites-available/wg-portal
  ln -sf /etc/nginx/sites-available/wg-portal /etc/nginx/sites-enabled/wg-portal
  rm -f /etc/nginx/sites-enabled/default

  systemctl daemon-reload
  systemctl enable wg-portal >/dev/null 2>&1 || true
  systemctl restart wg-portal

  if nginx -t >/dev/null 2>&1; then
    systemctl enable nginx >/dev/null 2>&1 || true
    systemctl restart nginx
  else
    echo "[wg-launch] WARNING: nginx config test failed; portal not served." >&2
  fi
fi

