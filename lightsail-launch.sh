chmod 600 /etc/wireguard/server_private.key /etc/wireguard/server_public.key

# Runtime config consumed by wg-manage.
cat > /etc/wireguard/wg-manage.conf <<CONF
# Generated by wireguard-launch. After editing, run: wg-manage rebuild-conf
WG_INTERFACE=${WG_INTERFACE}
WG_SUBNET=${WG_SUBNET}
WG_SERVER_IP=${WG_SERVER_IP}
WG_LISTEN_PORT=${WG_LISTEN_PORT}
WG_DNS=${WG_DNS}
WG_ALLOWED_IPS=${WG_ALLOWED_IPS}
WG_KEEPALIVE=${WG_KEEPALIVE}
WG_ENDPOINT_HOST=${WG_ENDPOINT_HOST}
CONF
